const (
	output        string = "output"
	testRequestID string = "xyz"
)

var (
	unaryInfo = &grpc.UnaryServerInfo{
		FullMethod: "TestService.UnaryMethod",
	}
	streamInfo = &grpc.StreamServerInfo{
		FullMethod:     "TestService.StreamMethod",
		IsServerStream: true,
	}

	errExpected = errors.New("expected error")
)

func TestUnaryServerWithoutRequestID(t *testing.T) {
	unaryHandler := func(ctx context.Context, req any) (any, error) {
		requestID := header.ExtractRequestID(ctx)
		if requestID == "" {
			t.Errorf("requestID must be generated by interceptor")
		}

		if got, want := header.ExtractRequestID(ctx), requestID; got != want {
			t.Errorf("expect requestID to %q, but got %q", want, got)
		}

		return output, nil
	}

	ctx := grpc.NewContextWithServerTransportStream(context.Background(), ServerTransportStream(""))

	if _, err := request.NewRequestIDMiddleware().Unary()(ctx, testRequestID, unaryInfo, unaryHandler); err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
}

func TestUnaryServerWithRequestID(t *testing.T) {
	assert := require.New(t)
	requestID, err := uuid.NewUUID()
	assert.NoError(err)

	unaryHandler := func(ctx context.Context, req any) (any, error) {
		assert.True(strings.HasPrefix(header.ExtractRequestID(ctx), requestID.String()))
		return output, nil
	}

	ctx := context.Background()
	md := metadata.Pairs(string(header.HeaderAsertoRequestID), requestID.String())
	ctx = grpc.NewContextWithServerTransportStream(ctx, ServerTransportStream(""))
	ctx = metadata.NewIncomingContext(ctx, md)

	_, err = request.NewRequestIDMiddleware().Unary()(ctx, testRequestID, unaryInfo, unaryHandler)
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
}

